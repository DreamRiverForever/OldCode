public class PeopleInfo {
    private double high;
    private double weight;

    public PeopleInfo(double high, double weight) {
        this.high = high;
        this.weight = weight;
    }

    public PeopleInfo() {
    }

    public double getHigh() {
        return high;
    }

    public double getWeight() {
        return weight;
    }

    public void setHigh(double high) {
        this.high = high;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
}
